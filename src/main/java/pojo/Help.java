package pojo;

public class Help {
    private int hId;
    private String hName;
    private String hLink;

    public int gethId() {
        return hId;
    }
    public void sethId(int hId) {
        this.hId = hId;
    }
    public String gethName() {
        return hName;
    }
    public void sethName(String hName) {
        this.hName = hName;
    }
    public String gethLink() {
        return hLink;
    }
    public void sethLink(String hLink) {
        this.hLink = hLink;
    }
}
