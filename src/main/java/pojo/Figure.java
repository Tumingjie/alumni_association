package pojo;

public class Figure {
    private int fId;
    private String fName;
    private String fLink;

    public int getfId() {
        return fId;
    }
    public void setfId(int fId) {
        this.fId = fId;
    }
    public String getfName() {
        return fName;
    }
    public void setfName(String fName) {
        this.fName = fName;
    }
    public String getfLink() {
        return fLink;
    }
    public void setfLink(String fLink) {
        this.fLink = fLink;
    }
}
