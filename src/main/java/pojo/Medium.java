package pojo;

public class Medium {
    private int mId;
    private String mName;
    private String mLink;

    public int getmId() {
        return mId;
    }
    public void setmId(int mId) {
        this.mId = mId;
    }
    public String getmName() {
        return mName;
    }
    public void setmName(String mName) {
        this.mName = mName;
    }
    public String getmLink() {
        return mLink;
    }
    public void setmLink(String mLink) {
        this.mLink = mLink;
    }
}
