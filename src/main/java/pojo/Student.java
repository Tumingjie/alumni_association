package pojo;

public class Student {
    private int sId;
    private String sName;
    private String sLink;

    public int getsId() {
        return sId;
    }
    public void setsId(int sId) {
        this.sId = sId;
    }
    public String getsName() {
        return sName;
    }
    public void setsName(String sName) {
        this.sName = sName;
    }
    public String getsLink() {
        return sLink;
    }
    public void setsLink(String sLink) {
        this.sLink = sLink;
    }
}
