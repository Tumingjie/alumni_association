package pojo;

public class News{

    private int nId;
    private String nName;
    private String nLink;

    public int getnId() {
        return nId;
    }
    public void setnId(int nId) {
        this.nId = nId;
    }
    public String getnName() {
        return nName;
    }

    public void setnName(String nName) {
        this.nName = nName;
    }

    public String getnLink() {
        return nLink;
    }

    public void setnLink(String nLink) {
        this.nLink = nLink;
    }
}
